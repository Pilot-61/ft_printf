#include "ft_printf.h"

void	ft_putnbru(unsigned int n, int *j)
{
	if (n >= 10)
	{
		ft_putnbru((n / 10), j);
		ft_putchar((n % 10) + '0', j);
	}
	else
		ft_putchar((n + '0'), j);
}
